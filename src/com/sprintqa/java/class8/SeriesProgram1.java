package com.sprintqa.java.class8;

public class SeriesProgram1 {
	public static void main(String[] args) {
		// Write a program to do sum of 1 to 10
		
//		0 + 1 + 2 + 3 + 4 + 5
		
		int sum=0;
		
		for(int i=1; i<=5; i++) {
			 sum = sum + i;
			 System.out.println("Iteration - "+i + " Sum="+sum + " Value of I="+i);
		}
		
		
		System.out.println("Sum value is "+sum);
		
		
		
		
		
		
		
		
	}
}
