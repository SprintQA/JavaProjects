package com.sprintqa.java.reviewclass;

public class Variable {

	public static void main(String[] args) {
		int num1;  // just declaring variable
		num1=10; // Assigning value to variable
		
		
		int num2 = 10;  // declaring and assigning value in single line
		
		
		//Declaring variable in different line
		int num3;
		int num4;
		int num5;
		
		// Declaring variables in single line
		int num6, num7,num8;
		
		// Declaring variables in single line and assigning value
		int num9=10, num10=20,num11=30;

	}

}
