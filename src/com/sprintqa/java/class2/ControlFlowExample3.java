package com.sprintqa.java.class2;

public class ControlFlowExample3 {

	public static void main(String args[]) {
		int fuelLevel = 1;

		System.out.println("1. Starting a ride");

		if (fuelLevel == 0) {

			System.out.println("2. Filling the fuel");

		} else {

			System.out.println("3. Skipping the petrol pump");

		}

		System.out.println("4. Reached destination");

	}

}
